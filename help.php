<h4><a name="9"></a> How to edit the status of remittance to PENDING/DELIVERED. </h4>
<p> 1. Click either View Money Remittance or View Package Remittance link on the navigation bar. <br/>
2. Results will be displayed, on the right hand side you will see "EDIT" button. CLick that. <br/>
3. Afterwards, option will be provided to change the status of the remittance.
 </p><br/>

<h4><a name="10"></a> How to archive remittances. </h4>
<p> 1. Click either View Money Remittance or View Package Remittance link on the navigation bar. <br/>
2. Results will be displayed, on the right hand side you will see "DELETE" button. CLick that. <br/>
3. Message will be displayed afterwards informing about the archiving of the remittance.
 </p><br/>



