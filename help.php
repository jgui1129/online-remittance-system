<h4> <a name="2"></a> How to send money remittance. </h4>
<p> 1. Click the Send Money Remittances link in the sidebar navigation.<br/>
2. Fill up the forms and click submit. <br/>
3. Once submitted, notification will be sent to the recepient via sms and email. <br/>
</p><br/>

<h4><a name="3"></a> How to receive money remittance. </h4>
<p> 1. Click the Claim Remittance link in the sidebar navigation.<br/>
2. Type the tracking number in the field provided. <br/>
3. Once enter, click the submit button and you will see the status and details of the remittance. <br/>
4. Click "CLAIM" button. <br/>
5. Sms notification will be sent to the sender's number informing them about the claimed remittance.
</p><br/>

<h4><a name="4"></a> How to send package remittance. </h4>
